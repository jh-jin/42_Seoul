#include "libft.h"

int	ft_atoi(const char *s)
{
	int			sign;
	long long	ret;

	ret = 0;
	sign = 1;
	while ((*s >= 7 && *s <= 13) || *s == 32)
		s++;
	if (*s == '-' || *s == '+')
		if (*s++ == '-')
			sign *= -1;
	while (*s >= '0' && *s <= '9')
		ret = ret * 10 + *s++ - '0';
	return ((int)(sign * ret));
}
